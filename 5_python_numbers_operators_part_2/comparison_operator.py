# Output dari comparision operator adalah boolean
# Hanya akan me-return nilai True atau False

number1 = 10
number2 = 15

# Sama dengan
print(number1 == number2)

# Tidak sama dengan
print(number1 != number2)

# Lebih besar dari
print(number1 > number2)

# Lebih kecil dari
print(number1 < number2)

# Lebih besar atau sama dengan
print(number1 >= number2)

# Lebih kecil atau sama dengan
print(number1 <= number2)