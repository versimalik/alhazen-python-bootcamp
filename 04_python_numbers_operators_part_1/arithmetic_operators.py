number1 = 15
number2 = 20

# Addition
add = number1 + number2
print(add)

# Substraction
subs = number1 - number2
print(subs)

# Multiplication
multipli = number1 * number2
print(multipli)

# Division
div = number1 / number2
print(div)

# Modulus
mod = 20 % 15
print(mod)

# Exponentiation
expo = 3 ** 4
print(expo)

# Floor Div
fd = 15 // 7
print(fd)